package com.day01.Entities;

public class Rest {
    private  String ApiId;
    private String Url;
    private String Type;


    public String getId() {
        return ApiId;
    }

    public void setId(String id) {
        ApiId = id;
    }

    public String getUrl() {
        return Url;
    }

    public void setUrl(String url) {
        Url = url;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }










}